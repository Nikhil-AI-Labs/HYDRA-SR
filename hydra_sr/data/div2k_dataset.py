"""
DIV2K + DF2K + LSDIR dataset loader for HYDRA-SR.

Supports both:
  - Clean bicubic degradation (Stage 1): HR→LR via Matlab-style bicubic
  - Real-world degradation (Stage 2+): LR generated by Real-ESRGAN pipeline

Data augmentation (following SR best practices):
  - Random crop to patch_size × patch_size (LR)
  - Horizontal flip (50%)
  - 90° rotation (25% each of {0°, 90°, 180°, 270°})
  - MixUp augmentation (optional, for Stage 2)

Patch size schedule (CutBlur curriculum from implementation plan):
  Stage 1: 64→96→128 (progressive during 400K iters)
  Stage 2: 64 (fixed, with real degradation)
  Stage 3: 64 (fixed)
"""

import os
import random
import numpy as np
import torch
from torch.utils.data import Dataset
from pathlib import Path


def read_img(path: str) -> np.ndarray:
    """Read image from path → numpy float32 in [0, 1] RGB."""
    import cv2
    img = cv2.imread(str(path), cv2.IMREAD_COLOR)
    if img is None:
        raise FileNotFoundError(f"Cannot read image: {path}")
    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    return img.astype(np.float32) / 255.0


def to_tensor(img: np.ndarray) -> torch.Tensor:
    """Convert HWC float32 numpy → CHW float32 tensor."""
    return torch.from_numpy(img.transpose(2, 0, 1)).contiguous()


def paired_random_crop(
    hr: np.ndarray,
    lr: np.ndarray,
    hr_patch_size: int,
    scale: int,
) -> tuple[np.ndarray, np.ndarray]:
    """Random crop of HR and corresponding LR patch."""
    hr_h, hr_w = hr.shape[:2]
    lr_patch_size = hr_patch_size // scale

    lr_h = hr_h // scale
    lr_w = hr_w // scale

    # Random top-left corner in LR space
    rnd_h = random.randint(0, max(0, lr_h - lr_patch_size))
    rnd_w = random.randint(0, max(0, lr_w - lr_patch_size))

    lr_patch = lr[rnd_h: rnd_h + lr_patch_size, rnd_w: rnd_w + lr_patch_size]
    hr_patch = hr[rnd_h * scale: (rnd_h + lr_patch_size) * scale,
                  rnd_w * scale: (rnd_w + lr_patch_size) * scale]
    return hr_patch, lr_patch


def augment(
    imgs: list[np.ndarray],
    hflip: bool = True,
    rotation: bool = True,
) -> list[np.ndarray]:
    """Apply random horizontal flip and 90° rotations to all images in list."""
    hflip_flag = hflip and random.random() < 0.5
    rot_k      = random.randint(0, 3) if rotation else 0

    result = []
    for img in imgs:
        if hflip_flag:
            img = img[:, ::-1, :]
        if rot_k > 0:
            img = np.rot90(img, k=rot_k, axes=(0, 1))
        result.append(np.ascontiguousarray(img))
    return result


def bicubic_downsample(
    hr: np.ndarray,
    scale: int,
) -> np.ndarray:
    """Downsample HR to LR using Matlab-style bicubic (cv2 INTER_CUBIC)."""
    import cv2
    h, w = hr.shape[:2]
    lr = cv2.resize(hr, (w // scale, h // scale), interpolation=cv2.INTER_CUBIC)
    return np.clip(lr, 0.0, 1.0)


class DIV2KDataset(Dataset):
    """
    Paired HR-LR dataset loader.

    Supports DIV2K, DF2K (DIV2K + Flickr2K), and LSDIR.
    For Stage 1 (bicubic): provide hr_root and lr_root (pre-downsampled).
    For Stage 2+ (real degradation): provide only hr_root and set
    use_real_degradation=True (LR is generated on-the-fly).

    Args:
        hr_root:               Path to HR images.
        lr_root:               Path to LR images (pre-downsampled). None for Stage 2+.
        patch_size:            LR patch size (default 64).
        scale:                 SR scale factor (default 4).
        train:                 If True, apply augmentation.
        use_real_degradation:  If True, generate LR on-the-fly (Stage 2+).
        real_deg_weight:       Fraction of real-degradation samples (0.0–1.0).
                               0.0 = all bicubic. 1.0 = all real.
    """

    def __init__(
        self,
        hr_root: str,
        lr_root: str = None,
        patch_size: int = 64,
        scale: int = 4,
        train: bool = True,
        use_real_degradation: bool = False,
        real_deg_weight: float = 0.0,
    ):
        super().__init__()
        self.hr_root    = Path(hr_root)
        self.lr_root    = Path(lr_root) if lr_root else None
        self.patch_size = patch_size
        self.scale      = scale
        self.train      = train
        self.use_real   = use_real_degradation
        self.real_weight = real_deg_weight

        # Scan for image files
        ext = {'.png', '.jpg', '.jpeg', '.bmp', '.tif', '.tiff'}
        self.hr_files = sorted([
            p for p in self.hr_root.rglob('*') if p.suffix.lower() in ext
        ])

        if self.lr_root is not None:
            self.lr_files = sorted([
                p for p in self.lr_root.rglob('*') if p.suffix.lower() in ext
            ])
            assert len(self.hr_files) == len(self.lr_files), (
                f"HR/LR file count mismatch: {len(self.hr_files)} vs {len(self.lr_files)}"
            )
        else:
            self.lr_files = None

        # Optional real-degradation pipeline (Stage 2+)
        if use_real_degradation:
            try:
                from .degradations import RealESRGANDegradation
                self.real_deg = RealESRGANDegradation(scale=scale)
            except ImportError:
                self.real_deg = None
        else:
            self.real_deg = None

    def __len__(self) -> int:
        return len(self.hr_files)

    def __getitem__(self, idx: int) -> dict:
        hr = read_img(self.hr_files[idx])

        # Decide degradation type
        use_real_this_sample = (
            self.real_deg is not None
            and random.random() < self.real_weight
        )

        if use_real_this_sample:
            lr, deg_vec = self.real_deg(hr)
        elif self.lr_files is not None:
            lr = read_img(self.lr_files[idx])
            deg_vec = np.array([0.5, 0.0, 95.0, 1.0], dtype=np.float32)  # clean bicubic
        else:
            lr = bicubic_downsample(hr, self.scale)
            deg_vec = np.array([0.5, 0.0, 95.0, 1.0], dtype=np.float32)

        if self.train:
            hr, lr = paired_random_crop(hr, lr, self.patch_size * self.scale, self.scale)
            hr, lr = augment([hr, lr], hflip=True, rotation=True)

        return {
            'lr':      to_tensor(lr),
            'hr':      to_tensor(hr),
            'deg_vec': torch.from_numpy(deg_vec),
            'hr_path': str(self.hr_files[idx]),
        }
